package javax.servlet.jsp.el;

public class ELException extends jakarta.el.ELException {
  public ELException() {
  }

  public ELException(String message) {
    super(message);
  }

  public ELException(Throwable cause) {
    super(cause);
  }

  public ELException(String message, Throwable cause) {
    super(message, cause);
  }
}
